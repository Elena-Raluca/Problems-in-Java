package canvas_shapes;

public class Point extends Shape {
    private float x;
    private float y;

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public void draw() {
        System.out.println("We draw the point with coordinates x= " + x + " and y=" + y);
    }
}

